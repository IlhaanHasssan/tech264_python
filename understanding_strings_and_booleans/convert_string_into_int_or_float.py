int_string = "6"

# convert int_string to an integer
int_int = int(int_string)
print(int_int)
# after converting, print its data type to the screen
print(type(int_int))

# convert int_string to float
int_float = float(int_string)
print(int_float)
# after converting, print its data type to the screen
print(type(int_float))
